function sortSelectOptions(options, dataType) {
    if (dataType === 'int') {
        return options.sort((a, b) => {
            if (Number(a) < Number(b)) {
                return -1;
            }
            return 1;
        });
    }
    return options.sort((a, b) => {
        if (a < b) {
            return -1;
        }
        return 1;
    });
}
function sortArray(arr, by, asc = true) {
    if (asc) {
        return arr.sort((a, b) => {
            if (a[by] < b[by]) {
                return -1;
            }
            return 1;
        });
    }
    return arr.sort((a, b) => {
        if (a[by] < b[by]) {
            return 1;
        }
        return -1;
    });
}

